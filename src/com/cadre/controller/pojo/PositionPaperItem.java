package com.cadre.controller.pojo;

import java.util.Date;


public class PositionPaperItem {
	private Integer index;
	
	private String actionType;//操作类型 1为增岗，2为撤岗，3为改岗增岗，4为改岗撤岗
	
	private String acadmy;
	
	private String department;
	
	private String positionName;
	
	private String positionLevel;
	
	private Date actionDay;

	private Integer isDelWithUser;
	
	private String positionType;
	
	private Integer positionId;
	
	public String getActionType() {
		return actionType;
	}

	public void setActionType(String actionType) {
		this.actionType = actionType;
	}

	public String getAcadmy() {
		return acadmy;
	}

	public void setAcadmy(String acadmy) {
		this.acadmy = acadmy;
	}

	public String getDepartment() {
		return department;
	}

	public void setDepartment(String department) {
		this.department = department;
	}

	public String getPositionName() {
		return positionName;
	}

	public void setPositionName(String positionName) {
		this.positionName = positionName;
	}

	public String getPositionLevel() {
		return positionLevel;
	}

	public void setPositionLevel(String positionLevel) {
		this.positionLevel = positionLevel;
	}

	public Date getActionDay() {
		return actionDay;
	}

	public void setActionDay(Date actionDay) {
		this.actionDay = actionDay;
	}

	public Integer getIsDelWithUser() {
		return isDelWithUser;
	}

	public void setIsDelWithUser(Integer isDelWithUser) {
		this.isDelWithUser = isDelWithUser;
	}

	public String getPositionType() {
		return positionType;
	}

	public void setPositionType(String positionType) {
		this.positionType = positionType;
	}

	public Integer getPositionId() {
		return positionId;
	}

	public void setPositionId(Integer positionId) {
		this.positionId = positionId;
	}

	public Integer getIndex() {
		return index;
	}

	public void setIndex(Integer index) {
		this.index = index;
	}
	
	
	
}
